def igual(a,b):
    if (a == b):
        return "Son iguales"
    else:
        return "No son iguales"

def diferente(a,b):
    if (a != b):
        return "Son diferentes"
    else:
        return "Son iguales"
    
def mayor(a,b):
    if (a > b):
        return "El primer número es mayor"
    else:
        return "El segundo número es mayor"

def menor(a,b):
    if (a < b):
        return "El primer número es menor"
    else:
        return "El segundo número es menor"

def mayorIgual(a,b):
    if (a >= b):
        return "El primer número es mayor o igual"
    else:
        return "El segundo número es mayor"

def menorIgual(a,b):
    if (a <= b):
        return "El primer número es menor o igual"
    else:
        return "El segundo número es mayor"